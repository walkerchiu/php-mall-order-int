     * Handle the model "created" event.
     *
     * @param Model  $model
     * @return void
     */
    public function created($model)
    {
        config('wk-core.class.mall-order.review')
            ::withTrashed()
            ->where('order_id', $model->order_id)
            ->where('id', '<>', $model->id)
            ->update(['is_current' => 0]);
    }

